/**
 * Generate a DOCX Blob for any supported document type.
 * Accepts InvoiceData as a catch-all (new types are passed through `data as any`
 * to access their specific fields, since they share the same storage shape).
 */
export async function generateDocx(data: InvoiceData): Promise<Blob> {
  const docType = (data.documentType || "invoice").toLowerCase()

  let doc: Document

  switch (docType) {
    case "sow":
      doc = generateSOWDocx(data as unknown as SOWData)
      break
    case "change_order":
      doc = generateChangeOrderDocx(data as unknown as ChangeOrderData)
      break
    case "nda":
      doc = generateNDADocx(data as unknown as NDAData)
      break
    case "client_onboarding_form":
      doc = generateClientOnboardingFormDocx(data as unknown as ClientOnboardingFormData)
      break
    case "payment_followup":
      doc = generatePaymentFollowupDocx(data as unknown as PaymentFollowupData)
      break
    default:
      // invoice, contract, quote, quotation, proposal, receipt — all use InvoiceData
      doc = generateInvoiceFamilyDocx(data)
      break
  }

  return Packer.toBlob(doc)
}
