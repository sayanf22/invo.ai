// ─── Payment Follow-up DOCX ────────────────────────────────────────────────

function generatePaymentFollowupDocx(data: PaymentFollowupData): Document {
  const title = `Payment Follow-up — ${data.invoiceNumber}`

  const fromLines = [data.fromName, data.fromEmail, data.fromAddress].filter(Boolean) as string[]
  const toLines = [data.toName, data.toEmail, data.toAddress].filter(Boolean) as string[]

  const toneLabel: Record<string, string> = {
    polite: "Polite Reminder",
    firm: "Firm Notice",
    urgent: "Urgent Notice",
  }

  const children: (Paragraph | Table)[] = [
    new Paragraph({
      heading: HeadingLevel.HEADING_1,
      spacing: { after: 120 },
      children: [new TextRun({ text: title, bold: true, size: 36, font: "Calibri", color: "1A1A1A" })],
    }),
    partiesTable("From", fromLines, "To", toLines),
    separator(),

    sectionHeading("Invoice Details"),
    labelValue("Invoice Number", data.invoiceNumber),
    labelValue("Invoice Amount", fmtMoney(data.invoiceAmount, data.invoiceCurrency)),
    labelValue("Due Date", data.dueDate),
    labelValue("Days Overdue", String(data.daysOverdue)),
    labelValue("Reminder Tone", toneLabel[data.reminderTone] || data.reminderTone),
    ...(data.paymentLinkUrl ? [labelValue("Payment Link", data.paymentLinkUrl)] : []),
    separator(),

    sectionHeading("Message"),
    bodyText(data.customMessage),
    separator(),

    ...(data.notes ? [sectionHeading("Notes"), bodyText(data.notes), separator()] : []),

    new Paragraph({ spacing: { after: 400 } }),
    docFooter(),
  ]

  return assembleDoc(title, "Payment Follow-up", children)
}

